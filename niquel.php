<h2>O Níquel foi descoberto por Alemães no século XVII, porém só passou a ser
utilizado a partir de 1820 quando Michael Faraday fez uma liga sintética de ferro-níquel.
Então começaram a extrair o níquel e utilizá-lo puro para proteger peças metálicas contra
a oxidação, para ser utilizado em material militar, em moedas, em meios de transporte e em
muitos tipos de aços especiais, como em aços inoxidáveis.
</h2>

