<h2>Na natureza não é encontrado o níquel de forma
pura, porém ele possui seus minerais, um deles são os
sulfetos (milerita e pentlandita (FeNi 9S8 ), que se
apresentam associados a outros sulfetos metálicos em
rochas básicas, geralmente acompanhados de cobre e cobalto.
O sulfeto é o principal mineral utilizado, contribuindo com
mais de 90% do níquel extraído. O outro mineral é a garnierita
ou silicato hidratado de níquel e magnésio, que se encontra associado
às rochas básicas, concentrando-se por processos de intemperismo nas partes alteradas.
</h2>

