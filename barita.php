<h2>A barita é um mineral de sulfato de bário. O seu nome tem origem no grego “barys” (pesado). Apesar de conter bário, um metal pesado, não é considerada tóxica devido à sua elevada insolubilidade. Comum em veios de chumbo e zinco em calcários, em depósitos hidrotermais e associada a minérios de hematita. Muitas vezes ocorre associada à celestita e à anglesita.
 

Cerca de 90% da produção de barita é utilizada para aumentar a densidade das lamas de perfuração na indústria petrolífera. Também é utilizada na fabricação de tintas e de papel. O Brasil possui 0,3 % das reservas mundiais e sua produção atinge 1% da produção mundial.

</h2>

