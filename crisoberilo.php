<h2>É composta por berílio e alumínio, mas diferentemente do berílio, ela é aluminato de berílio.
É um mineral raro, associando-se frequentemente ao berilo e à fenacite. Os pegmatitos com crisoberilo
encontram-se, frequentemente, instalados em domínios metamórficos, sendo eles micaxistos com elevado
grau de metamorfismo ou intersectando rochas ultrabásicas. No primeiro, o crisoberilo ocorre no interior
dos corpos pegmatíticos ou nas rochas encaixantes; no segundo, surge em orlas de reacção no contacto com as formações ultrabásicas.
As condições de formação do crisoberilo, relativamente ao berilo, exigem ambientes com muito maior concentração de alumina.
</h2>

