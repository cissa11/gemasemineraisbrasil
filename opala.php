<h2>A palavra opala vem do sânscrito upala, do Grego opallios e do latim opalus,
significando "pedra preciosa". Existem vários tipos de opala, pelo fato de ser encontrada
em várias cores, tais como: opala leite (um azulado leitoso a esverdeado); opala resina
(mel-amarelo com um lustre resinoso); opala madeira (causado pela recolocação do material
orgânico na madeira com opala); Menilite (marrom ou cinza) e hialite, uma opala vidro-desobstruído
incolor chamado às vezes Vidro de Müller.  Frequentemente muitas destas cores podem ser vistas
conjuntamente, em decorrência da interferência e difração da luz que passa entre aberturas
regularmente arranjadas dentro do microestrutura do opala, conhecido como difração de Bragg
(Retículo de Bragg). 
</h2>
