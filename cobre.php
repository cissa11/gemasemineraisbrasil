<h2>O cobre é um metal raro e foi o primeiro metal a ser utilizado para substituir a pedra.
É composto por apenas um elemento químico, o Cu, que é o próprio cobre. O cobre assim como o níquel
não é encontrado de forma pura na natureza. Ele é obtido através de um processo que o separa dos
minerais que estiverem junto dele. Geralmente é extraído o cobre do mineral Cu2S, que é a calcosita.
O cobre produzido a partir dela tem 99,9% de pureza.

