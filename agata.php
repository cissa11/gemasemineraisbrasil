<h2>   Na formação de uma ágata ordinária, é provável
que as águas que contêm sílica dissolvida – derivada da decomposição de alguns dos silicatos presentes
na própria lava – infiltraram-se através da rocha,
depositando um revestimento silicioso no interior das
vesículas produzidas por vapor. As variações no caráter
da solução, ou nas condições de deposição, podem causar
variações correspondentes nas camadas sucessivas, de modo
que as faixas de calcedônia frequentemente alternam com
camadas de quartzo cristalino.
</h2>

