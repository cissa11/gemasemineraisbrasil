<h2>A maioria das ágatas ocorre como nódulos em rochas
eruptivas, ou antigas lavas, onde preenchem as cavidades
produzidas originalmente pela desagregação do vapor na massa
derretida, e então preenchido, completamente ou parcialmente,
pela matéria silicosa depositada em camadas regulares em cima
das paredes. Tais ágatas, quando cortadas transversalmente,
exibem uma sucessão de linhas paralelas, frequentemente de extrema
tenuidade, dando uma aparência unida à seção, e por isso tais minerais
são conhecidas como ágata unida e ágata listrada.
 
</h2>
