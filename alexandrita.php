                    <h2>A alexandrita ou alexandrite, também conhecida como “esmeralda dos urais”
                        é uma pedra preciosa de grande valor e muito estimada pelo mercado das pedras
                        preciosas. Dependendo da luz, a alexandrite varia sua cor: à luz natural, normalmente
                        é encontrada em verde-oliva, mas em uma luz artificial (lâmpada incandescente) adquire
                        uma cor vermelha. Esta instigante mudança de cor deve-se ao fato de que a transmissão
                        da luz nas regiões do vermelho e verde-azul do espectro visível é praticamente a mesma
                        nesta gema, de modo que qualquer câmbio na natureza da luz incidente altera este
                        equilíbrio em favor de uma delas.
