<h2>O nome da pedra vem da primeira localidade de onde ela foi descoberta,
São José da Batalha(PB), em 1980. E apesar de existirem várias turmalinas
em outras partes do Brasil e do mundo, a Turmalina Paraíba é a única que
consegue alcançar teores de cobre acima de 2%.  Em outras partes do mundo,
como no Rio Grande do Norte e na África, os teores de cobre nas turmalinas
não chegam a 0.80%. Ocorre na forma de pequenos "cristais" na maioria das
vezes irregulares dentro de corpos pegmatíticos que na localidade estão encaixados
em quartzitos da Formação Equador (Grupo Seridó).
</h2>
