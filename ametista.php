<h2>Essa pedra preciosa é uma variedade gemológica
do quartzo transparente e semitransparente, de coloração
púrpura ou violeta. A origem de seu nome é do grego a,
"não" e methuskein, "intoxicar".  Ela é encontrada em pegmatitos
(um tipo de rocha  ígnea) e em cavidades de rochas vulcânicas.
Geralmente é usada como gema e em objetos ornamentais.
Seus cristais podem atingir dimensões tão grandes que,
no museu Britânico, está exposto um cristal de 250 quilogramas.
 
