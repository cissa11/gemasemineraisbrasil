<h2>   Se uma ametista ficar muito tempo exposta
ao sol sua cor pode enfraquecer. Isso ocorre com
frequência com as ametistas no Pará. Com o uso de
raios X a cor da ametista enfraquecida, pode ser
recuperada. Quando aquecida, aproximadamente a 475°C,
a ametista pode transformar-se em citrino, um quartzo
de cor amarela, laranja, ou (raramente) vermelha. Por
esse processo, o Rio Grande do Sul, produz muito citrino,
porém, é bom lembrar que existe o citrino natural e que nem
todo citrino é obtido pela ametista. Tais cristais ocorrem
tanto em cavidades alongadas (veios) em rochas ígneas, geralmente
basaltos, como revestindo internamente (geodos) de ágata.
