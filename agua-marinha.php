<h2>Este último tipo é muito comum no Brasil, onde existem três grandes
depósitos do berilo. Eles ficam nos estados da Bahia (tipo granítico e
placers aluviais – água marinha), Rio Grande do Norte (tipo granítico)
e no Espírito Santo e sudeste de Minas Gerais (placers aluviais – água marinha).<br>

O nome da gema vem do latim, aquamarine,  que ao entrar em contato com
a luz o ferro que contém na gema faz com que ela crie esse azul-esverdeado
e lembre dos mares. Assim como o Diamante e a Esmeralda, a Água Marinha tem
origem na consolidação do magma, devido à grande pressão que a lava recebe
no interior da terra, que fazem com que ela se solidifique e se transform
e em pedras.
</h2>

