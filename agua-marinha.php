<h2>Por ter as suas maiores jazidas aqui no Brasil, a água-marinha
é uma das gemas representativa do país. Tal gema deriva do mineral
berilo, que é incolor em sua composição particular, mas que possui
algumas variações conhecidas como, por exemplo, a esmeralda(que é o
berilo verde), a morganita(berilo rosa), e a própria água-marinha
que é o berilo azul-esverdeado. Ao redor do mundo existem dois tipos
de depósitos principais de berilo : os do tipo vulcânico – quase que
estritamente restrito aos Estados Unidos, que são portadores da bertrandita,
um outro mineral que contém o berílio) – e o tipo granítico, que é portador
do berilo comum ou industrial.
</h4>

