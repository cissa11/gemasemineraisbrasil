<h2>   Sua formação revela uma interessante história
de preenchimento de rachaduras existentes nas montanhas
por água rica em substâncias químicas, que conseguem
dissolver a rocha em pequenas partículas. Esse material,
ao chegar internamente na caverna, submete-se a um novo
processo químico, e começa a precipitar e gotejar lentamente,
formando a estalactite. Cada estalactite é gerada com um formato
único, que ao encostar-se uma na outra se fundem e formam uma rocha.
O Ônix é justamente essa rocha, formada pela união das estalactites,
e por isso é tão raro, de difícil extração e de beleza incomparável.<br><br>
  A pedra ônix pode ser usado na confecção de camafeus, um tipo de técnica
de escultura usada em jóias de modo que forma uma figura em relevo a partir
de cores sobrepostas de cores diferentes. O Camafeus é geralmente usado para
a confecção de broches.
</h2>

