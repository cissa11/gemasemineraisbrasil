<h2>A pedra ônix é um tipo de calcedônia,
ou seja, uma variação de quartzo. Dispõe de
diferentes cores, mas a cor com que é mais
apreciada é a preta. Suas cores são distribuídas
por faixas retas e paralelas. Seu nome vem do grego, onyx, que significa unha, pois os antigos gregos
acreditavam que, as pedras, eram as unhas de Vênus,
que foram cortadas por Cupido e caíram sobre a Terra.
Sua formação leva milhões de anos e também é conhecida
como alabastro. Diferente de outras
rochas, o Ônix é formado nas cavernas e é estruturado
a partir de subsequentes reações químicas que auxiliam
na magia da transformação. 
</h2>
