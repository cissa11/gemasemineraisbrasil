<h2>O carvão mineral é formado através de uma transformação de restos vegetais soterrados há milhões de anos atrás. Na formação do carvão podemos observar quatro fases, sendo elas: Turfa onde ocorre a deposição e putrefação dos restos vegetais em pântanos ou várzeas; Linhito é a segunda fase onde já pode ser encontrado um material escuro porém com muita água e pouco carbono; Hulha é a terceira fase onde já se encontra o carvão,preto e sólido, que pode ser transformado em carvão metalúrgico; Antracito é a última etapa e apresenta um alto teor de carbono, correspondente a 90-96%, possui coloração preta, brilhoso e alta dureza.

</h2>

