<h2>Gipsite ou gipsita, também chamada pedra de gesso, gesso ou sulfato de cálcio hidratado, é um minério de cálcio cuja composição química corresponde à fórmula Ca 2H₂O. O gipsite é, basicamente, composta por sulfato de cálcio hidratado. Apresenta geralmente coloração de branca a translúcida. Outras características são os aspectos micáceo, lamelar, o brilho nacarado, o tato untuoso (ou fibroso) e a dureza baixa (2,0). É o sulfato mais comum na crosta terrestre, ocorrendo em evaporitos ou na forma de camadas interestratificadas de folhelhos, calcário e argila, podendo também ser encontrado em meteoritos.
</h2>

