<h2>Como o amianto integra diferentes características
ele possui resistência a altas temperaturas, incombustibilidade,
boa isolação térmica, boa isolação acústica, flexibilidade e
maleabilidade para ser tecido. Além disso seu custo é relativamente
baixo e é encontrado em abundância na natureza. Utilizado na produção de telhas, caixas d’água, freios, canos, pisos e tetos.
</h2>

