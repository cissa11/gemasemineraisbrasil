<h2>Conhecido popularmente como amianto, o asbesto, é uma variedade fibrosa de sais minerais metamórficos,
encontrados na natureza. Podendo ser encontrado de duas maneiras, sendo uma delas a serpentina que
são fibras curvas e maleáveis conhecido como amianto branco, a outra é anfibólios, que são fibras
rígidas e retilíneas, que é conhecido como amianto escuro (marrons e azuis). Ele é extraído de rochas
de silicatos e hidratos de magnésio.
</h2>

